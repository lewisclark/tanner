package org.doubt.tanner;

public final class Config {
	private Hide hide;
	private Location location;

	public Hide getHide() {
		return hide;
	}

	public void setHide(Hide hide) {
		this.hide = hide;
	}

	public Location getLocation() {
		return location;
	}

	public void setLocation(Location location) {
		this.location = location;
	}
}